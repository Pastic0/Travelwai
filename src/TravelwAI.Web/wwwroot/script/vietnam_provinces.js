  if (!ethnic) return "";
  const lower = ethnic.toLowerCase();
  if (lower.startsWith("dân tộc") || lower.startsWith("người") || lower.startsWith("cộng đồng") || lower.startsWith("các dân tộc")) return ` (${ethnic})`;
