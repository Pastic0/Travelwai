const LOCAL_PROVINCE_INFO_STORAGE_VERSION_34 = 7;
const LOCAL_PROVINCE_INFO_STORAGE_KEY_34 = `travelwai_static_province_info_34_v${LOCAL_PROVINCE_INFO_STORAGE_VERSION_34}`;
const LOCAL_PROVINCE_INFO_OLD_STORAGE_KEYS_34 = Object.freeze([
  "travelwai_static_province_info_34",
  "travelwai_static_province_info_34_v1",
  "travelwai_static_province_info_34_v2",
  "travelwai_static_province_info_34_v3",
  "travelwai_static_province_info_34_v4",
  "travelwai_static_province_info_34_v5",
  "travelwai_static_province_info_34_v6"
]);

function cleanupOldProvinceLocalStorage34() {
  try {
    if (!window.localStorage) return;

    LOCAL_PROVINCE_INFO_OLD_STORAGE_KEYS_34.forEach((key) => {
      if (key && key !== LOCAL_PROVINCE_INFO_STORAGE_KEY_34) {
        localStorage.removeItem(key);
      }
    });

    for (let index = localStorage.length - 1; index >= 0; index -= 1) {
      const key = localStorage.key(index);
      if (!key || key === LOCAL_PROVINCE_INFO_STORAGE_KEY_34) continue;
      if (/^travelwai_static_province_info_34(?:_v\d+)?$/i.test(key)) {
        localStorage.removeItem(key);
      }
    }
  } catch (error) {
    console.warn("Không thể xoá dữ liệu tỉnh/thành local cũ:", error);
  }
}

function getAllLocalProvinceInfo34(date = new Date()) {
  return (Array.isArray(VIETNAM_34_PROVINCES) ? VIETNAM_34_PROVINCES : [])
    .map((item) => getLocalProvinceInfo(item.name, date))
    .filter(Boolean);
}

function getStaticProvinceStorageDate34(date = new Date()) {
  const year = date.getFullYear();
  const month = String(date.getMonth() + 1).padStart(2, "0");
  const day = String(date.getDate()).padStart(2, "0");
  return `${year}-${month}-${day}`;
}

function saveAllLocalProvinceInfo34(date = new Date()) {
  cleanupOldProvinceLocalStorage34();
  try {
    if (!window.localStorage) return null;
    const payload = {
      version: LOCAL_PROVINCE_INFO_STORAGE_VERSION_34,
      saved_date: getStaticProvinceStorageDate34(date),
      saved_at: new Date().toISOString(),
      source: "local",
      data: getAllLocalProvinceInfo34(date)
    };
    localStorage.setItem(LOCAL_PROVINCE_INFO_STORAGE_KEY_34, JSON.stringify(payload));
    return payload;
  } catch (error) {
    console.warn("Không thể lưu dữ liệu tỉnh/thành local:", error);
    return null;
  }
}

function readAllLocalProvinceInfo34(date = new Date()) {
  cleanupOldProvinceLocalStorage34();
  try {
    if (!window.localStorage) return null;
    const raw = localStorage.getItem(LOCAL_PROVINCE_INFO_STORAGE_KEY_34);
    if (!raw) return null;
    const payload = JSON.parse(raw);
    if (!payload || payload.version !== LOCAL_PROVINCE_INFO_STORAGE_VERSION_34 || payload.saved_date !== getStaticProvinceStorageDate34(date) || !Array.isArray(payload.data)) {
      return null;
    }
    return payload.data;
  } catch (error) {
    console.warn("Không thể đọc dữ liệu tỉnh/thành local:", error);
    return null;
  }
}

function refreshCurrentFestivalFields34(info, date = new Date()) {
  if (!info) return info;
  const provinceName = info.province_name || info.name || "";
  return {
    ...info,
    current_festivals: getCurrentFestivalItems34(provinceName, date),
    current_festival_summary: getCurrentFestivalSummary34(provinceName, date)
  };
}

function getStaticProvinceInfoFromLocal34(provinceName, date = new Date()) {
  const canonicalName = getCanonicalProvinceName34(provinceName);
  const cached = readAllLocalProvinceInfo34(date) || saveAllLocalProvinceInfo34(date)?.data || [];
  const match = cached.find((item) => getCanonicalProvinceName34(item?.province_name || item?.name || "") === canonicalName);
  return refreshCurrentFestivalFields34(match || getLocalProvinceInfo(canonicalName || provinceName, date), date);
}

