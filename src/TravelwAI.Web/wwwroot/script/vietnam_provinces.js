const PROVINCE_ALIAS_TO_34 = new Map();
VIETNAM_34_PROVINCES.forEach((province) => {
  PROVINCE_ALIAS_TO_34.set(normalizeProvinceKey(province.name), province.name);
  (province.merged_from || []).forEach((alias) => PROVINCE_ALIAS_TO_34.set(normalizeProvinceKey(alias), province.name));
  (province.aliases || []).forEach((alias) => PROVINCE_ALIAS_TO_34.set(normalizeProvinceKey(alias), province.name));
});

function normalizeProvinceKey(value) {
  return (value || "")
    .toString()
    .trim()
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .replace(/đ/g, "d")
    .replace(/[–—]/g, "-")
    .replace(/[^a-z0-9\s.-]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function getCanonicalProvinceName34(provinceName) {
  const key = normalizeProvinceKey(provinceName);
  return PROVINCE_ALIAS_TO_34.get(key) || provinceName;
}

