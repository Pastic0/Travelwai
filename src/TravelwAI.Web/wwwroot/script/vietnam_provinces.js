function normalizeFestivalCalendarKey34(value) {
  const raw = String(value || "lunar")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .trim();

  if (["solar", "duong", "duong lich", "dl", "gregorian", "dương lịch"].includes(raw)) return "solar";
  if (["lunar", "am", "am lich", "al", "âm lịch"].includes(raw)) return "lunar";
  return raw.includes("duong") || raw.includes("solar") ? "solar" : "lunar";
}

function normalizeFestivalCalendar34(value) {
  return normalizeFestivalCalendarKey34(value) === "solar" ? "dương lịch" : "âm lịch";
}

function formatFestivalMonth34(month) {
  if (month === "hằng tháng") return "hằng tháng";
  if (Array.isArray(month)) return month.map((value) => String(value).padStart(2, "0")).join("-");
  const value = String(month || "").trim();
  if (/^\d+$/.test(value)) return value.padStart(2, "0");
  return value.replace(/\d+/g, (match) => match.padStart(2, "0"));
}

